"""
RAG Experiment Platform — 实验平台主入口
=========================================

一站式实验调度器，整合所有评测维度：
  1. Retrieval Recall    → evaluate.py（兼容旧版）
  2. Ranking Quality     → ranking_eval.py（MRR / Hit@K）
  3. Ablation Study      → ablation.py（模块贡献量化）
  4. Hard Cases          → hard_cases.json（困难样本专项）
  5. End-to-End QA       → qa_eval.py（LLM 回答质量）

使用方式：
    uv run python -m ai_app1.eval.experiment [command]

命令：
    recall      运行传统召回评测
    ranking     运行排序质量评测
    ablation    运行消融实验
    hard        运行困难样本评测
    qa          运行端到端 QA 评测
    all         运行全部评测（完整实验报告）

设计目标：
  - 一键输出完整实验报告（Markdown + JSON）
  - 配置驱动，无需修改代码即可切换实验参数
  - 结果可对比、可追踪、可复现
"""
from __future__ import annotations

import argparse
import asyncio
import json
import sys
import time
from pathlib import Path
from typing import Callable

_PROJECT_ROOT = Path(__file__).parent.parent.parent
if str(_PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(_PROJECT_ROOT))

from ai_app1.eval.metrics import EvalMetrics
from ai_app1.service.vector_store import RetrievalConfig


# ─── 路径常量 ─────────────────────────────────────────────────────────────────

_EVAL_DIR = Path(__file__).parent
_REPORT_DIR = _EVAL_DIR / "reports"
_REPORT_DIR.mkdir(parents=True, exist_ok=True)


# ─── 各评测模块的 lazy import（避免启动时加载模型）─────────────────────────────

def _run_recall_eval() -> dict:
    """运行传统召回评测（兼容旧版 evaluate.py）。"""
    from ai_app1.eval.evaluate import run_eval
    # run_eval 直接打印到 stdout，不返回结构化数据
    # 这里通过复用 ranking_eval 的 aggregate 来获取结构化结果
    from ai_app1.eval.ranking_eval import run_ranking_eval
    metrics = run_ranking_eval(verbose=False)
    return {
        "name": "recall",
        "metrics": metrics.to_dict(),
    }


def _run_ranking_eval() -> dict:
    """运行排序质量评测。"""
    from ai_app1.eval.ranking_eval import run_ranking_eval
    metrics = run_ranking_eval(verbose=False)
    return {
        "name": "ranking",
        "metrics": metrics.to_dict(),
    }


def _run_ablation() -> dict:
    """运行消融实验。"""
    from ai_app1.eval.ablation import run_ablation_study
    results = run_ablation_study(verbose_per_query=False)
    return {
        "name": "ablation",
        "experiments": results,
    }


def _run_hard_cases() -> dict:
    """运行困难样本专项评测。"""
    from ai_app1.eval.ranking_eval import run_ranking_eval
    hard_path = _EVAL_DIR / "hard_cases.json"
    metrics = run_ranking_eval(
        dataset_path=hard_path,
        verbose=False,
    )
    return {
        "name": "hard_cases",
        "metrics": metrics.to_dict(),
    }


async def _run_qa_eval() -> dict:
    """运行端到端 QA 评测。"""
    from ai_app1.eval.qa_eval import run_qa_eval
    report = await run_qa_eval(verbose=False)
    return {
        "name": "qa",
        "report": report,
    }


# ─── 报告生成 ─────────────────────────────────────────────────────────────────

def _generate_full_report(results: list[dict]) -> str:
    """生成完整实验报告的 Markdown。"""
    ts = time.strftime("%Y-%m-%d %H:%M:%S")
    lines = [
        "# RAG Experiment Report\n",
        f"**生成时间**: {ts}  ",
        f"**项目**: ai_app1 RAG Evaluation Platform\n",
        "---\n",
    ]

    for r in results:
        name = r["name"]
        lines.append(f"## {name.upper()}\n")

        if name in ("recall", "ranking", "hard_cases"):
            m = r.get("metrics", {})
            lines.append(f"- **Recall@5**: {m.get('recall@5', 0):.1%}")
            lines.append(f"- **MRR**: {m.get('mrr', 0):.3f}")
            lines.append(f"- **Hit@1**: {m.get('hit@1', 0):.1%}")
            lines.append(f"- **Hit@3**: {m.get('hit@3', 0):.1%}")
            lines.append(f"- **Hit@5**: {m.get('hit@5', 0):.1%}")
            lat = m.get("latency_ms", {})
            lines.append(f"- **平均延迟**: {lat.get('mean', 0):.0f}ms (P95={lat.get('p95', 0):.0f}ms)")

        elif name == "ablation":
            lines.append(EvalMetrics.markdown_header())
            for exp in r.get("experiments", []):
                md = EvalMetrics(**{
                    k: v for k, v in exp["metrics"].items() if k != "latency_ms"
                })
                md.latency = _latency_from_dict(exp["metrics"]["latency_ms"])
                md.config_summary = exp["config"]
                lines.append(md.to_markdown_row())

        elif name == "qa":
            rep = r.get("report", {})
            lines.append(f"- **平均 Overall**: {rep.get('avg_overall', 0):.3f}")
            lines.append(f"- **平均 Coverage**: {rep.get('avg_coverage', 0):.3f}")
            lines.append(f"- **平均 Hallucination**: {rep.get('avg_hallucination', 0):.3f}")
            lines.append(f"- **平均延迟**: {rep.get('avg_latency_ms', 0):.0f}ms")

        lines.append("")

    lines.append("---\n")
    lines.append("*Report generated by ai_app1.eval.experiment*\n")
    return "\n".join(lines)


def _latency_from_dict(d: dict):
    from ai_app1.eval.metrics import LatencyStats
    return LatencyStats(
        mean=d.get("mean", 0.0),
        std=d.get("std", 0.0),
        p50=d.get("p50", 0.0),
        p95=d.get("p95", 0.0),
        p99=d.get("p99", 0.0),
        min=d.get("min", 0.0),
        max=d.get("max", 0.0),
        count=d.get("count", 0),
    )


# ─── 主调度器 ─────────────────────────────────────────────────────────────────

async def run_experiment(command: str = "all") -> None:
    """
    实验平台主调度器。

    Args:
        command: recall | ranking | ablation | hard | qa | all
    """
    print(f"\n{'='*70}")
    print(f"  RAG Experiment Platform   command={command}")
    print(f"{'='*70}\n")

    start = time.perf_counter()
    results: list[dict] = []

    # 定义命令映射
    runners: dict[str, Callable] = {
        "recall": _run_recall_eval,
        "ranking": _run_ranking_eval,
        "ablation": _run_ablation,
        "hard": _run_hard_cases,
    }
    async_runners: dict[str, Callable] = {
        "qa": _run_qa_eval,
    }

    if command == "all":
        commands = ["recall", "ranking", "ablation", "hard", "qa"]
    else:
        commands = [command]

    for cmd in commands:
        print(f"▶ 执行: {cmd} ...")
        try:
            if cmd in runners:
                res = runners[cmd]()
            elif cmd in async_runners:
                res = await async_runners[cmd]()
            else:
                print(f"  ⚠ 未知命令: {cmd}，跳过")
                continue
            results.append(res)
            print(f"  ✓ 完成\n")
        except Exception as e:
            print(f"  ✗ 失败: {e}\n")
            results.append({"name": cmd, "error": str(e)})

    total_elapsed = (time.perf_counter() - start) * 1000

    # 生成报告
    md_report = _generate_full_report(results)
    ts = time.strftime("%Y%m%d_%H%M%S")
    md_path = _REPORT_DIR / f"experiment_{ts}.md"
    with open(md_path, "w", encoding="utf-8") as f:
        f.write(md_report)

    json_path = _REPORT_DIR / f"experiment_{ts}.json"
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump({
            "timestamp": time.strftime("%Y-%m-%d %H:%M:%S"),
            "total_time_ms": round(total_elapsed, 2),
            "results": results,
        }, f, ensure_ascii=False, indent=2)

    print(f"{'='*70}")
    print(f"  实验完成  总耗时: {total_elapsed:.0f}ms")
    print(f"  报告已保存:")
    print(f"    Markdown: {md_path}")
    print(f"    JSON:     {json_path}")
    print(f"{'='*70}\n")

    # 打印 Markdown 摘要到控制台
    print(md_report)


# ─── CLI 入口 ─────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="RAG Experiment Platform — 一键运行多维度评测",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
示例:
  uv run python -m ai_app1.eval.experiment recall
  uv run python -m ai_app1.eval.experiment ranking
  uv run python -m ai_app1.eval.experiment ablation
  uv run python -m ai_app1.eval.experiment hard
  uv run python -m ai_app1.eval.experiment qa
  uv run python -m ai_app1.eval.experiment all
        """,
    )
    parser.add_argument(
        "command",
        nargs="?",
        default="all",
        choices=["recall", "ranking", "ablation", "hard", "qa", "all"],
        help="要执行的评测命令（默认 all）",
    )
    args = parser.parse_args()
    asyncio.run(run_experiment(args.command))


if __name__ == "__main__":
    main()
