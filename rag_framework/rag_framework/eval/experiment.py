"""
RAG 通用实验平台主入口

一站式实验调度器，整合所有评测维度：
  1. Retrieval Ranking → rag_framework.eval.ranking
  2. Ablation Study    → rag_framework.eval.ablation
  3. Hard Cases        → 困难样本专项
  4. End-to-End QA     → rag_framework.eval.qa
"""
from __future__ import annotations

import argparse
import asyncio
import json
import time
from pathlib import Path
from typing import Callable

from rag_framework.container import RAGContainer
from rag_framework.core.config import get_settings
from rag_framework.eval.ablation import run_ablation_study
from rag_framework.eval.metrics import EvalMetrics, LatencyStats
from rag_framework.eval.qa import run_qa_eval
from rag_framework.eval.ranking import run_ranking_eval


_REPORT_DIR = Path("reports")
_REPORT_DIR.mkdir(parents=True, exist_ok=True)


# ─── 各评测模块的 lazy import wrapper ───────────────────────────────────────────

def _run_ranking_eval(container: RAGContainer | None = None) -> dict:
    metrics = run_ranking_eval(container=container, verbose=False)
    return {"name": "ranking", "metrics": metrics.to_dict()}


def _run_ablation(container: RAGContainer | None = None) -> dict:
    results = run_ablation_study(container=container, verbose_per_query=False)
    return {"name": "ablation", "experiments": results}


def _run_hard_cases(container: RAGContainer | None = None, hard_path: Path | None = None) -> dict:
    metrics = run_ranking_eval(container=container, dataset_path=hard_path, verbose=False)
    return {"name": "hard_cases", "metrics": metrics.to_dict()}


async def _run_qa_eval(container: RAGContainer | None = None, dataset_path: Path | None = None) -> dict:
    report = await run_qa_eval(dataset_path=dataset_path, container=container, verbose=False)
    return {"name": "qa", "report": report}


# ─── 报告生成 ───────────────────────────────────────────────────────────────────

def _generate_full_report(results: list[dict]) -> str:
    ts = time.strftime("%Y-%m-%d %H:%M:%S")
    lines = [
        "# RAG Experiment Report\n",
        f"**生成时间**: {ts}  ",
        f"**项目**: RAG Framework Evaluation Platform\n",
        "---\n",
    ]

    for r in results:
        name = r["name"]
        lines.append(f"## {name.upper()}\n")

        if name in ("ranking", "hard_cases"):
            m = r.get("metrics", {})
            lines.append(f"- **Recall@5**: {m.get('recall@5', 0):.1%}")
            lines.append(f"- **MRR**: {m.get('mrr', 0):.3f}")
            lines.append(f"- **Hit@1**: {m.get('hit@1', 0):.1%}")
            lines.append(f"- **Hit@3**: {m.get('hit@3', 0):.1%}")
            lines.append(f"- **Hit@5**: {m.get('hit@5', 0):.1%}")
            lat = m.get("latency_ms", {})
            lines.append(f"- **平均延迟**: {lat.get('mean', 0):.0f}ms (P95={lat.get('p95', 0):.0f}ms)")

        elif name == "ablation":
            lines.append(EvalMetrics.markdown_header())
            for exp in r.get("experiments", []):
                md = EvalMetrics(**{
                    k: v for k, v in exp["metrics"].items() if k != "latency_ms"
                })
                md.latency = _latency_from_dict(exp["metrics"]["latency_ms"])
                md.config_summary = exp["config"]
                lines.append(md.to_markdown_row())

        elif name == "qa":
            rep = r.get("report", {})
            lines.append(f"- **平均 Overall**: {rep.get('avg_overall', 0):.3f}")
            lines.append(f"- **平均 Coverage**: {rep.get('avg_coverage', 0):.3f}")
            lines.append(f"- **平均 Hallucination**: {rep.get('avg_hallucination', 0):.3f}")
            lines.append(f"- **平均延迟**: {rep.get('avg_latency_ms', 0):.0f}ms")

        lines.append("")

    lines.append("---\n")
    lines.append("*Report generated by rag_framework.eval.experiment*\n")
    return "\n".join(lines)


def _latency_from_dict(d: dict) -> LatencyStats:
    return LatencyStats(
        mean=d.get("mean", 0.0),
        std=d.get("std", 0.0),
        p50=d.get("p50", 0.0),
        p95=d.get("p95", 0.0),
        p99=d.get("p99", 0.0),
        min=d.get("min", 0.0),
        max=d.get("max", 0.0),
        count=d.get("count", 0),
    )


# ─── 主调度器 ───────────────────────────────────────────────────────────────────

async def run_experiment(
    command: str = "all",
    container: RAGContainer | None = None,
    hard_cases_path: Path | None = None,
    qa_dataset_path: Path | None = None,
) -> None:
    """
    实验平台主调度器。

    Args:
        command: ranking | ablation | hard | qa | all
        container: RAG 容器，默认自动构建
        hard_cases_path: 困难样本评测集路径
        qa_dataset_path: QA 评测集路径
    """
    print(f"\n{'='*70}")
    print(f"  RAG Experiment Platform   command={command}")
    print(f"{'='*70}\n")

    start = time.perf_counter()
    results: list[dict] = []

    runners: dict[str, Callable] = {
        "recall": lambda: _run_ranking_eval(container),
        "ranking": lambda: _run_ranking_eval(container),
        "ablation": lambda: _run_ablation(container),
        "hard": lambda: _run_hard_cases(container, hard_cases_path),
    }
    async_runners: dict[str, Callable] = {
        "qa": lambda: _run_qa_eval(container, qa_dataset_path),
    }

    if command == "all":
        commands = ["recall", "ranking", "ablation", "hard", "qa"]
    else:
        commands = [command]

    for cmd in commands:
        print(f"▶ 执行: {cmd} ...")
        try:
            if cmd in runners:
                res = runners[cmd]()
            elif cmd in async_runners:
                res = await async_runners[cmd]()
            else:
                print(f"  ⚠ 未知命令: {cmd}，跳过")
                continue
            results.append(res)
            print(f"  ✓ 完成\n")
        except Exception as e:
            print(f"  ✗ 失败: {e}\n")
            results.append({"name": cmd, "error": str(e)})

    total_elapsed = (time.perf_counter() - start) * 1000

    # 生成报告
    md_report = _generate_full_report(results)
    ts = time.strftime("%Y%m%d_%H%M%S")
    md_path = _REPORT_DIR / f"experiment_{ts}.md"
    with open(md_path, "w", encoding="utf-8") as f:
        f.write(md_report)

    json_path = _REPORT_DIR / f"experiment_{ts}.json"
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump({
            "timestamp": time.strftime("%Y-%m-%d %H:%M:%S"),
            "total_time_ms": round(total_elapsed, 2),
            "results": results,
        }, f, ensure_ascii=False, indent=2)

    print(f"{'='*70}")
    print(f"  实验完成  总耗时: {total_elapsed:.0f}ms")
    print(f"  报告已保存:")
    print(f"    Markdown: {md_path}")
    print(f"    JSON:     {json_path}")
    print(f"{'='*70}\n")

    print(md_report)


# ─── CLI 入口 ───────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="RAG Experiment Platform — 一键运行多维度评测",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
示例:
  python -m rag_framework.eval.experiment ranking
  python -m rag_framework.eval.experiment ablation
  python -m rag_framework.eval.experiment all
        """,
    )
    parser.add_argument(
        "command",
        nargs="?",
        default="all",
        choices=["recall", "ranking", "ablation", "hard", "qa", "all"],
        help="要执行的评测命令（默认 all）",
    )
    args = parser.parse_args()
    asyncio.run(run_experiment(args.command))


if __name__ == "__main__":
    main()
